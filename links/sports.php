			<p><b><h3>Baseball:</h3></b> is a bat-and-ball sport played between two teams of nine players each, taking turns batting and fielding. The game is live when the umpire signals to the pitcher either verbally or by pointing, indicating that the ball is now in play. A player on the fielding team, called the pitcher, throws a ball that a player on the batting team tries to hit with a bat. The objective of the offensive team (batting team) is to hit the ball into the field of play, away from the other team's players, allowing its players to run the bases, having them advance counter-clockwise around four bases to score what are called "runs". The objective of the defensive team (referred to as the fielding team) is to prevent batters from becoming runners, and to prevent runners' advance around the bases.</p>
